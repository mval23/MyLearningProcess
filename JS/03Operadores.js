//A R I T M E T I C O S
//Suma y resta *
var x = 9;
var y = 3;
x += y
console.log(x)

//Producto *
var x = 9;
var y = 3;
x *= y
console.log(x)

//Div /
var x = 9;
var y = 3;
x /= y
console.log(x)

//Modulo %
var x = 10;
var y = 3;
x %= y
console.log(x)

//Potencia **
var x = 2;
var y = 3;
x **= y
console.log(x)


// C O M P A R A C I O N
// equal ==
console.log(3 == 3);
console.log(3 == 2);
console.log(3 == '3');

// strict ====
console.log(3 === 3);
console.log(3 === 2);
console.log(3 === '3'); //tipo de dato // tipado

// not equal
console.log(3 != 3);
console.log(3 != 2);
console.log(3 != '3');

console.log(3 !== 3);
console.log(3 !== 2);
console.log(3 !== '3'); //tipo de dato // tipado
